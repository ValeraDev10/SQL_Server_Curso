--MOSTRAR LA CANTIDAD, PRECIO Y STOCK DE LA TABLA ORDER DETAIL CALCULAR MONTO TOTAL CANTIDAD POR
--PRECIO MENOS EL DESCUENTO

SELECT * FROM [Order Details];


SELECT Quantity,UnitPrice, Discount,
       Quantity * (UnitPrice - (UnitPrice * Discount )) AS MONTOTOTAL
FROM [Order Details];

SELECT Quantity,UnitPrice, Discount,
       Quantity * UnitPrice - Discount AS MONTOTOTAL
FROM [Order Details];


